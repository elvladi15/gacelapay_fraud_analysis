WITH CTE AS
(
	SELECT
		ID,
		GET_ML_RESULT(IS_FRAUD::BOOLEAN, FLAGGED::BOOLEAN)	AS ML_RESULT
	FROM
		joined_tables_for_analysis
),
CTE2 AS
(
	SELECT
		JTA.ID,
		CASE WHEN FLAGGED = 1 THEN 0 ELSE UNPROCESSED_USD_FEES END	AS USD_FEES,
		CASE
			WHEN ML_RESULT = 'FALSE NEGATIVE' THEN -JTA.TRANSACTION_USD_AMOUNT
			ELSE 0
		END	AS FRAUD_LOSS,
		CASE
			WHEN ML_RESULT = 'TRUE NEGATIVE' THEN 0
			WHEN ML_RESULT = 'TRUE POSITIVE' THEN -ROUND((20 - 5) * JTA.COST_VARIATION_FACTOR + 5, 2)
			WHEN ML_RESULT = 'FALSE POSITIVE' THEN -ROUND((12 - 2) * JTA.COST_VARIATION_FACTOR + 2, 2)
			WHEN ML_RESULT = 'FALSE NEGATIVE' THEN -ROUND((30 - 10) * JTA.COST_VARIATION_FACTOR + 10, 2)
		END	AS INVESTIGATION_COST,
		CASE
			WHEN ML_RESULT = 'TRUE NEGATIVE' THEN 0
			WHEN ML_RESULT = 'TRUE POSITIVE' THEN -ROUND((15 - 3) * JTA.COST_VARIATION_FACTOR + 3, 2)
			WHEN ML_RESULT = 'FALSE POSITIVE' THEN -ROUND((25 - 8) * JTA.COST_VARIATION_FACTOR + 8, 2)
			WHEN ML_RESULT = 'FALSE NEGATIVE' THEN -ROUND((40 - 10) * JTA.COST_VARIATION_FACTOR + 10, 2)
		END	AS CUSTOMER_SUPPORT_COST,
		CASE
			WHEN ML_RESULT = 'TRUE NEGATIVE' THEN 0
			WHEN ML_RESULT = 'TRUE POSITIVE' THEN -ROUND((3 - 0) * JTA.COST_VARIATION_FACTOR + 0, 2)
			WHEN ML_RESULT = 'FALSE POSITIVE' THEN -ROUND((4 - 0.5) * JTA.COST_VARIATION_FACTOR + 0.5, 2)
			WHEN ML_RESULT = 'FALSE NEGATIVE' THEN -ROUND((15 - 3) * JTA.COST_VARIATION_FACTOR + 3, 2)
		END	AS LOST_INTERCHANGE
	FROM
		joined_tables_for_analysis	JTA
		INNER JOIN CTE ON JTA.ID = CTE.ID
)
SELECT
	JTA.ID,
	JTA.DATE,
	JTA.IS_FRAUD,
	JTA.FLAGGED,
	ML_RESULT,
	USD_FEES,
	FRAUD_LOSS,
	INVESTIGATION_COST,
	CUSTOMER_SUPPORT_COST,
	LOST_INTERCHANGE,
	USD_FEES + FRAUD_LOSS + INVESTIGATION_COST +CUSTOMER_SUPPORT_COST + LOST_INTERCHANGE	AS GRAND_TOTAL
FROM
	joined_tables_for_analysis	JTA
	INNER JOIN CTE ON JTA.ID = CTE.ID
	INNER JOIN CTE2 ON JTA.ID = CTE2.ID