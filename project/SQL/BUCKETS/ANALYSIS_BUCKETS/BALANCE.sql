WITH CTE AS
(
	SELECT
		CASE
			WHEN BALANCE < 5000 THEN '<5000'
			WHEN BALANCE >= 5000 AND BALANCE < 10000 THEN '5000-10000'
			WHEN BALANCE >= 10000 AND BALANCE < 15000 THEN '10000-15000'
			WHEN BALANCE >= 15000 AND BALANCE < 20000 THEN '15000-20000'
			WHEN BALANCE >= 20000 AND BALANCE < 25000 THEN '20000-25000'
			WHEN BALANCE >= 25000 AND BALANCE < 30000 THEN '25000-30000'
			WHEN BALANCE >= 30000 THEN '>=30000'
		END	AS BUCKET,
		IS_FRAUD
	FROM
		joined_tables_for_analysis
)
SELECT
	BUCKET,
	SUM(IS_FRAUD)	AS IS_FRAUD_QUANTITY,
	COUNT(*)		AS TRANSACTION_QUANTITY
FROM
	CTE
GROUP BY
	BUCKET;