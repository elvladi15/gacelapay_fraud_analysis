WITH CTE AS
(
	SELECT
		CASE
			WHEN TRANSACTION_USD_AMOUNT < 2000 THEN '<2000'
			WHEN TRANSACTION_USD_AMOUNT >= 2000 AND TRANSACTION_USD_AMOUNT < 4000 THEN '2000-4000'
			WHEN TRANSACTION_USD_AMOUNT >= 4000 AND TRANSACTION_USD_AMOUNT < 6000 THEN '4000-6000'
			WHEN TRANSACTION_USD_AMOUNT >= 6000 AND TRANSACTION_USD_AMOUNT < 8000 THEN '6000-8000'
			WHEN TRANSACTION_USD_AMOUNT >= 8000 AND TRANSACTION_USD_AMOUNT < 10000 THEN '8000-10000'
			WHEN TRANSACTION_USD_AMOUNT >= 10000 THEN '>=10000'
		END	AS BUCKET,
		IS_FRAUD
	FROM
		joined_tables_for_analysis
)
SELECT
	BUCKET,
	SUM(IS_FRAUD)	AS IS_FRAUD_QUANTITY,
	COUNT(*)		AS TRANSACTION_QUANTITY
FROM
	CTE
GROUP BY
	BUCKET;